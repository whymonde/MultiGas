#include "timer.h"


#define TIM4Period      8
#define TIM4Prescaler   9

void TIM4_Int_Init(void)
{
	TIM_TimeBaseInitTypeDef TIM_TimeBaseInitStructure;
	
	RCC_APB1PeriphClockCmd(RCC_APB1Periph_TIM4,ENABLE);  ///使能TIM1时钟
	
	TIM_TimeBaseInitStructure.TIM_Period = TIM4Period; 	//自动重装载值
	TIM_TimeBaseInitStructure.TIM_Prescaler=TIM4Prescaler;  //定时器分频
	TIM_TimeBaseInitStructure.TIM_CounterMode=TIM_CounterMode_Up; //向上计数模式
	TIM_TimeBaseInitStructure.TIM_ClockDivision=0; 
	TIM_TimeBaseInitStructure.TIM_RepetitionCounter=0; 
	
	TIM_TimeBaseInit(TIM4,&TIM_TimeBaseInitStructure);//初始化TIM1
	TIM_ARRPreloadConfig(TIM4,ENABLE); //使能定时器1
	
}

void delay_us(int Times)//u16 Times
{
TIM4->CR1 |=TIM_CR1_CEN;
while(Times--)
{
while((TIM4->SR & TIM_FLAG_Update)==RESET);
TIM4->SR &= ~TIM_FLAG_Update;
}
TIM4->CR1 &=~TIM_CR1_CEN;
}
 
void delay_ms(u16 nms)
{
while(nms--)
 {
  delay_us(1000);
 }
}

